CREATE OR REPLACE FUNCTION XMLSOURCE_NUMCHECK (
  P_INPUT IN VARCHAR2)
RETURN NUMBER DETERMINISTIC
IS

 V_OUTPUT NUMBER;

BEGIN

  BEGIN

    V_OUTPUT := TO_NUMBER(P_INPUT);

  EXCEPTION

    WHEN OTHERS THEN
      IF SQLCODE = -6502
      THEN
        V_OUTPUT := TO_NUMBER(NULL);
      ELSE
        RAISE;
      END IF;

  END;

  RETURN V_OUTPUT;

END XMLSOURCE_NUMCHECK;
/
